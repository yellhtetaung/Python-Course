print("bool(0) ", bool(0))  # False
print("bool(1) ", bool(1))  # True
print("bool(0.0) ", bool(0.0))  # False
print("bool(1.0) ", bool(1.0))  # True
print("bool('False')", bool("False"))  # True
print("bool('')", bool(''))  # False
print("bool([])", bool([]))  # False
print("bool([1])", bool([1]))  # True
print("bool(())", bool(()))  # False
